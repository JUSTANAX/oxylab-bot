def build_changelog_text() -> str:
    lines = ["📋 <b>Обновления</b>\n"]
    for entry in CHANGELOG[:10]:
        lines.append(f"🔖 <b>v{entry['version']}</b> — {entry['date']}")
        for change in entry["changes"]:
            lines.append(f"  • {change}")
        lines.append("")
    return "\n".join(lines).rstrip()
